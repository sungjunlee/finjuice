"""End-to-end tests with synthetic Banksalad data.

This test suite validates the complete pipeline workflow using synthetic
transaction data that mimics Banksalad exports. It measures performance,
accuracy, and generates comprehensive reports.

The synthetic data is generated by scripts/generate_e2e_fixtures.py and includes:
- Transactions matching sample_rules.yaml patterns (cafes, convenience stores, etc.)
- Transfer pairs for pairing algorithm testing
- Untagged merchants for rule effectiveness analysis
- 3 months of data for partition testing

Tests:
1. Clean pipeline run with synthetic data
2. Incremental update (idempotency)
3. Performance benchmarks
4. Rule effectiveness analysis
5. Transfer detection accuracy
6. Report generation and validation
"""

import shutil
from datetime import datetime
from pathlib import Path

import pytest

from finjuice.pipeline.export.master import export_master_xlsx
from finjuice.pipeline.export.reports import generate_all_reports
from finjuice.pipeline.ingest.pipeline import ingest_all_files, ingest_file
from finjuice.pipeline.storage import csv_partition
from finjuice.pipeline.tagging.pipeline import run_tagging
from finjuice.pipeline.transfer.detection import run_transfer_detection

# Test data paths - using synthetic data for reproducible CI testing
SYNTHETIC_DATA_SOURCE = Path("tests/fixtures/e2e/synthetic_banksalad_e2e.xlsx")
SAMPLE_RULES = Path("tests/fixtures/sample_rules.yaml")


@pytest.fixture
def real_data_dir(tmp_path: Path) -> Path:
    """Create a temporary data directory with synthetic test data.

    Args:
        tmp_path: pytest temporary directory

    Returns:
        Path to test data directory with structure:
        - imports/synthetic_banksalad_e2e.xlsx
        - transactions/ (CSV partitions)
        - rules.yaml
        - exports/
        - exports/reports/
    """
    # Create directory structure
    data_dir = tmp_path / "data"
    imports_dir = data_dir / "imports"
    csv_base_dir = data_dir / "transactions"
    exports_dir = data_dir / "exports"
    reports_dir = exports_dir / "reports"

    imports_dir.mkdir(parents=True)
    csv_base_dir.mkdir(parents=True)
    reports_dir.mkdir(parents=True)

    # Copy synthetic test data
    if not SYNTHETIC_DATA_SOURCE.exists():
        pytest.skip(
            f"Synthetic data not found: {SYNTHETIC_DATA_SOURCE}. "
            "Run 'uv run python scripts/generate_e2e_fixtures.py' to generate."
        )

    shutil.copy(SYNTHETIC_DATA_SOURCE, imports_dir / SYNTHETIC_DATA_SOURCE.name)

    # Copy sample rules
    if not SAMPLE_RULES.exists():
        pytest.skip(f"Sample rules not found: {SAMPLE_RULES}")

    shutil.copy(SAMPLE_RULES, data_dir / "rules.yaml")

    return data_dir


@pytest.mark.e2e
def test_clean_pipeline_with_real_data(real_data_dir: Path) -> None:
    """Test complete pipeline run with real anonymized data.

    Validates:
    - All phases complete successfully
    - Tag coverage meets >60% target
    - Transfer pairs are detected
    - All reports are generated
    """
    # Setup paths
    csv_base_dir = real_data_dir / "transactions"
    imports_dir = real_data_dir / "imports"
    rules_path = real_data_dir / "rules.yaml"
    exports_dir = real_data_dir / "exports"
    reports_dir = exports_dir / "reports"

    # Phase 1: Ingest
    ingest_summary = ingest_all_files(imports_dir, csv_base_dir)
    assert ingest_summary["files"] > 0, "No files ingested"
    assert ingest_summary["inserted"] > 0, "No transactions inserted"

    # Phase 2: Tag
    tag_summary = run_tagging(csv_base_dir, rules_path)
    assert tag_summary["total"] > 0, "No transactions to tag"
    assert tag_summary["tagged"] > 0, "No transactions were tagged"

    # Phase 3: Transfer Detection
    transfer_summary = run_transfer_detection(csv_base_dir)
    assert transfer_summary["candidates"] >= 0, "Invalid transfer candidate count"

    # Phase 4: Export
    today = datetime.now().strftime("%Y%m%d")
    master_path = exports_dir / f"master_{today}.xlsx"
    row_count = export_master_xlsx(csv_base_dir, master_path)
    reports_summary = generate_all_reports(csv_base_dir, reports_dir)

    assert master_path.exists(), "Master XLSX not created"
    assert row_count > 0, "Master XLSX is empty"
    assert reports_summary["reports"] == 5, "Not all reports generated"


@pytest.mark.e2e
def test_synthetic_fixture_edge_cases(real_data_dir: Path) -> None:
    """The synthetic fixture must exercise every edge-case class on ingest.

    Guards issue #699: the E2E fixture always carries duplicate rows, transfer
    pairs, malformed rows, and tagging edge cases, so the suite keeps testing
    deduplication, row-level validation, transfer detection, and rule coverage.
    """
    csv_base_dir = real_data_dir / "transactions"
    imports_dir = real_data_dir / "imports"
    rules_path = real_data_dir / "rules.yaml"
    xlsx_path = imports_dir / SYNTHETIC_DATA_SOURCE.name

    inserted, skipped_dedup, skipped_rows = ingest_file(xlsx_path, csv_base_dir)

    # Duplicate rows are deduplicated by row_hash on ingest.
    assert skipped_dedup >= 3, f"expected >=3 deduplicated rows, got {skipped_dedup}"
    # Malformed rows are skipped row-by-row without aborting the file.
    assert len(skipped_rows) >= 3, f"expected >=3 skipped malformed rows, got {skipped_rows}"
    assert inserted > 0, "no valid transactions ingested"

    # Tagging edge cases: both tagged and untagged transactions are present.
    tag_summary = run_tagging(csv_base_dir, rules_path)
    assert tag_summary["tagged"] > 0, "no tagged transactions"
    assert tag_summary["tagged"] < tag_summary["total"], "expected some untagged transactions"

    # Transfer pairs are detected.
    transfer_summary = run_transfer_detection(csv_base_dir)
    assert transfer_summary["pairs"] > 0, "no transfer pairs detected"


@pytest.mark.e2e
def test_incremental_update(real_data_dir: Path) -> None:
    """Test incremental pipeline run (idempotency).

    Validates:
    - Running pipeline twice produces identical results
    - No duplicate transactions created
    - Tags remain consistent
    - Transfer pairs remain consistent
    """
    csv_base_dir = real_data_dir / "transactions"
    imports_dir = real_data_dir / "imports"
    rules_path = real_data_dir / "rules.yaml"

    # First run
    ingest_all_files(imports_dir, csv_base_dir)
    run_tagging(csv_base_dir, rules_path)
    run_transfer_detection(csv_base_dir)

    # Collect first run state (Polars)
    df1 = csv_partition.get_all_transactions(csv_base_dir)
    count1 = len(df1)
    hashes1 = set(df1["row_hash"].to_list())
    tags1 = {row["row_hash"]: row["tags_final"] for row in df1.iter_rows(named=True)}

    # Second run (should be idempotent)
    ingest_all_files(imports_dir, csv_base_dir)
    run_tagging(csv_base_dir, rules_path)
    run_transfer_detection(csv_base_dir)

    # Collect second run state (Polars)
    df2 = csv_partition.get_all_transactions(csv_base_dir)
    count2 = len(df2)
    hashes2 = set(df2["row_hash"].to_list())
    tags2 = {row["row_hash"]: row["tags_final"] for row in df2.iter_rows(named=True)}

    # Validate idempotency
    assert count1 == count2, f"Transaction count changed: {count1} → {count2}"
    assert hashes1 == hashes2, "Row hashes differ between runs"
    assert tags1 == tags2, "Tags differ between runs"


